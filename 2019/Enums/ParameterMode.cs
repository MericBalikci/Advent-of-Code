/// <summary>
/// Mode of the parameter.
/// </summary>
public enum ParameterMode
{
    /// <summary>
    /// The value at the pointer's position.
    /// </summary>
    Position,

    /// <summary>
    /// The value at the immediate position.
    /// </summary>
    Immediate,

    /// <summary>
    /// The value according to relative base
    /// </summary>
    Relative
}